//Question: Write a program that takes an array of names and logs each name to the console using a loop.

//    Example:
//    Input: `['Alice', 'Bob', 'Charlie']`
//    Output:

//    ```
//    Alice
//    Bob
//    Charlie
//    ```

const names = ['Alice', 'Bob', 'Charlie'];

for (let i = 0; i < names.length; i++) {
    console.log(names[i]);
}