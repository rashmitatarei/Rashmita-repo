// 1. Question: Given an array of numbers `[1, 2, 3, 4, 5]`, write a loop to calculate the sum of all the numbers in the array.

//    Example:
//    Input: `[1, 2, 3, 4, 5]`
//    Output: `15`

const num = [1, 2, 3, 4, 5];
let sum = 0;

for (let i = 0; i < num.length; i++) {
    sum += num[i];
}

console.log(sum);               

        

